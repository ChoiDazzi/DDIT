package chapter02.chapter024;


public class VariableExample {
	public static void main(String[] args) {
		//변수의 선언
		byte age; // byte는 8bit (-128~127)
		double value;
		int number;
		
		//변수의 초기화
		age = 10; // 왼 = 오 : 오른쪽의 값을 왼쪽에 저장
		value = 3.14;
		number = 27; 
		//변수는 선언 및 초기화가 되어야 사용(읽을) 수 있다. 
	}
}
